    $('#create-recipe').hide()
    $('#create-button').hide()
    $('#show-recipe').hide()
    $('#show-button').hide()
    $('#update-recipe').hide()
    $('#update-button').hide()
    $('#index-user').hide()
    $('#index-all').hide()
    $('#destroy-recipe').show()
    $('#destroy-button').hide()
